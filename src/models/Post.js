import mongoose from "mongoose";


const postSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    content: {
      type: String,
      required: true,
    },
    viewCount: {
      type: Number,
      default: 0,
    },
    isPublished: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);



const Post = mongoose.model("Post", postSchema);
// const Product = mongoose.model("Product", productSchema);
export default Post;